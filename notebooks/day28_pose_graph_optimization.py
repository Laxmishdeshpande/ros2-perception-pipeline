## ═══════════════════════════════════════════════════════════
## CELL 5: Define the Pose Graph Error Function
## ═══════════════════════════════════════════════════════════

def pose_graph_error(poses_flat, edges, loop_closures):
    """
    THE CORE OF POSE GRAPH OPTIMIZATION.

    This function computes the error for EVERY edge in the graph.
    The optimizer will call this repeatedly, adjusting poses_flat
    each time, trying to make these errors as small as possible.

    Think of it like this:
    - Each edge says "pose B should be HERE relative to pose A"
    - We check: "given where A and B actually are, is that true?"
    - The difference = error
    - Optimizer nudges poses until all differences are tiny

    It's like checking every spring in the network:
    - Measure how stretched each spring is (error)
    - Report all stretches to the optimizer
    - Optimizer adjusts ball positions to reduce total stretch

    poses_flat: all poses flattened into 1D array [x0,y0,θ0, x1,y1,θ1, ...]
    edges: list of (from_idx, to_idx, measurement, confidence)
    loop_closures: list of (from_idx, to_idx, measurement, confidence)
    """
    # Reshape flat array back into (N, 3) — each row is [x, y, theta]
    poses = poses_flat.reshape(-1, 3)
    errors = []

    # ─── VO Edge Errors ───────────────────────────────────
    for (i, j, measurement, confidence) in edges:
        # What the measurement PREDICTS: relative pose from i to j
        predicted = compute_relative_pose(poses[i], poses[j])

        # Error = difference between measurement and prediction
        err = (predicted - measurement) * confidence
        errors.extend(err)

    # ─── Loop Closure Edge Errors ─────────────────────────
    for (i, j, measurement, confidence) in loop_closures:
        predicted = compute_relative_pose(poses[i], poses[j])

        # Loop closure gets HIGHER confidence — we trust it
        # because it was verified with geometric matching
        err = (predicted - measurement) * confidence
        errors.extend(err)

    return np.array(errors)


# ─── Build the edge list ──────────────────────────────────
# VO edges: consecutive poses, moderate confidence
vo_confidence = 1.0    # baseline confidence for VO measurements

edges = []
for i in range(num_poses - 1):
    edges.append((i, i + 1, vo_measurements[i], vo_confidence))

print(f"VO edges: {len(edges)}")

# ─── Loop Closure Edge ────────────────────────────────────
# Pose 7 should be close to pose 0 (robot returned to start)
# This measurement says: "from pose 7, pose 0 is roughly HERE"
# We compute it from ground truth (simulating loop closure detection)
# and give it HIGH confidence — loop closure was geometrically verified

loop_measurement = compute_relative_pose(ground_truth[-1], ground_truth[0])

# Add small noise to loop closure too — it's not perfect either
loop_noise = np.random.randn(3) * noise_std * 0.5  # less noise than VO
loop_measurement_noisy = loop_measurement + loop_noise

# Loop closure gets 3x the confidence of VO edges
# WHY? It was verified with feature matching + RANSAC (like your Day 20)
# High inlier ratio = high confidence = stiff spring
loop_confidence = 3.0

loop_closures = [(num_poses - 1, 0, loop_measurement_noisy, loop_confidence)]

print(f"Loop closure edges: {len(loop_closures)} (pose {num_poses-1} → pose 0)")
print(f"Loop closure confidence: {loop_confidence}x (stiffer spring)")

## ═══════════════════════════════════════════════════════════
## CELL 6: Run the Optimizer!
## ═══════════════════════════════════════════════════════════

# Start with the drifted poses as initial guess
# The optimizer will adjust these to minimize total error
initial_guess = drifted_poses.copy().flatten()

# Pin pose 0 — don't let the optimizer move the starting position
# WHY? Without an anchor, the optimizer could slide the ENTIRE
# trajectory somewhere random and still have zero edge error.
# It's like solving a jigsaw puzzle — you need at least one piece
# fixed to the table. Pose 0 is our anchor.

def pose_graph_error_pinned(poses_flat, edges, loop_closures, anchor_pose):
    """
    Same as pose_graph_error but with an extra constraint:
    Pose 0 must stay at its original position.
    """
    errors = pose_graph_error(poses_flat, edges, loop_closures)

    # Anchor constraint: heavily penalize moving pose 0
    # confidence=10 makes this a very stiff spring
    poses = poses_flat.reshape(-1, 3)
    anchor_error = (poses[0] - anchor_pose) * 10.0
    errors = np.concatenate([errors, anchor_error])

    return errors


anchor = ground_truth[0]    # pin pose 0 to true start

# ─── RUN OPTIMIZATION ─────────────────────────────────────
# least_squares implements Levenberg-Marquardt algorithm
# (a variant of Gauss-Newton — the standard solver for pose graphs)
# It calls our error function repeatedly, adjusting poses each time,
# until the total error stops decreasing (converged to the valley)

print("Running pose graph optimization...")
print(f"Initial total error: {np.sum(pose_graph_error_pinned(initial_guess, edges, loop_closures, anchor)**2):.4f}")

result = least_squares(
    pose_graph_error_pinned,      # function that computes errors
    initial_guess,                # starting poses (drifted)
    args=(edges, loop_closures, anchor),  # extra arguments
    method='lm',                  # Levenberg-Marquardt (Gauss-Newton variant)
    verbose=0                     # quiet output
)

# Extract optimized poses
optimized_poses = result.x.reshape(-1, 3)

print(f"Final total error:   {np.sum(result.fun**2):.4f}")
print(f"Optimization converged: {result.success}")
print(f"Iterations: {result.nfev}")

# Show improvement
opt_drift = optimized_poses[-1] - ground_truth[-1]
print(f"\nBefore optimization — drift: {np.sqrt(drift[0]**2 + drift[1]**2):.3f}m")
print(f"After optimization  — drift: {np.sqrt(opt_drift[0]**2 + opt_drift[1]**2):.3f}m")

