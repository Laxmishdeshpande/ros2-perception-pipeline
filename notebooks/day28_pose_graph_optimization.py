import numpy as np
import matplotlib.pyplot as plt
from scipy.optimize import least_squares

# ─── Ground Truth Poses ───────────────────────────────────
# 8 poses forming a square path (2D for simplicity)
# Each pose = [x, y, theta]  (position + heading angle)
# Robot walks: right → right → down → down → left → left → up → up
# Ends back where it started — a perfect loop

ground_truth = np.array([
    [0.0, 0.0, 0.0],          # Pose 0: start at origin, facing right
    [1.0, 0.0, 0.0],          # Pose 1: moved 1m right
    [2.0, 0.0, -np.pi/2],     # Pose 2: moved 1m right, turned to face down
    [2.0, -1.0, -np.pi/2],    # Pose 3: moved 1m down
    [2.0, -2.0, np.pi],       # Pose 4: moved 1m down, turned to face left
    [1.0, -2.0, np.pi],       # Pose 5: moved 1m left
    [0.0, -2.0, np.pi/2],     # Pose 6: moved 1m left, turned to face up
    [0.0, -1.0, np.pi/2],     # Pose 7: moved 1m up — should be near pose 0!
])

num_poses = len(ground_truth)

print(f"Ground truth: {num_poses} poses forming a 2m x 2m square")
print(f"Start: {ground_truth[0]}")
print(f"End:   {ground_truth[-1]} (should be close to start)")

## ═══════════════════════════════════════════════════════════
## CELL 2: Create Noisy VO Measurements
## ═══════════════════════════════════════════════════════════

def compute_relative_pose(pose_from, pose_to):
    """
    Compute the relative measurement between two poses.
    This is what VO gives us: "to get from pose A to pose B,
    move by (dx, dy) and rotate by dtheta."

    Think of it like: I'm standing at A, facing some direction.
    How far forward/sideways do I need to walk, and how much
    do I need to turn, to reach B?
    """
    dx = pose_to[0] - pose_from[0]       # x difference in world frame
    dy = pose_to[1] - pose_from[1]       # y difference in world frame
    dtheta = pose_to[2] - pose_from[2]   # rotation change

    # Rotate the translation into pose_from's local frame
    # WHY: VO measures motion in the CAMERA'S frame, not the world
    # Same reason we did R_global @ t_step in vo_node
    cos_t = np.cos(pose_from[2])
    sin_t = np.sin(pose_from[2])
    local_dx = cos_t * dx + sin_t * dy   # forward in local frame
    local_dy = -sin_t * dx + cos_t * dy  # sideways in local frame

    return np.array([local_dx, local_dy, dtheta])


# ─── Generate VO measurements with noise ──────────────────
# Each measurement = true relative pose + random noise
# This simulates what real VO gives you: correct on average,
# but slightly wrong every time. Errors accumulate.

np.random.seed(42)                # reproducible results

# Noise level — how much each VO measurement is off
# 0.05m position noise, 0.03 radian (~2°) angle noise
# Real VO is roughly this noisy on each step
noise_std = np.array([0.05, 0.05, 0.03])

# Create measurements between consecutive poses (VO edges)
vo_measurements = []
for i in range(num_poses - 1):
    # True relative motion between pose i and pose i+1
    true_relative = compute_relative_pose(ground_truth[i], ground_truth[i+1])

    # Add noise — this is the VO measurement error
    noise = np.random.randn(3) * noise_std
    noisy_relative = true_relative + noise

    vo_measurements.append(noisy_relative)
    print(f"VO edge {i}→{i+1}: dx={noisy_relative[0]:.3f}, "
          f"dy={noisy_relative[1]:.3f}, dθ={np.degrees(noisy_relative[2]):.1f}°")

print(f"\n{len(vo_measurements)} VO measurements created (each slightly noisy)")

## ═══════════════════════════════════════════════════════════
## CELL 3: Chain Measurements → Drifted Trajectory
## ═══════════════════════════════════════════════════════════

def chain_poses(start_pose, measurements):
    """
    Chain relative measurements to build a trajectory.
    This is EXACTLY what vo_node does:
      t_global = t_global + R_global @ t_step
      R_global = R_global @ R_step

    In 2D it's simpler — just rotate local motion by heading angle
    and add to current position.

    The problem: each measurement has small error.
    Chain 8 small errors → big accumulated drift.
    Like copying a photocopy of a photocopy — quality degrades.
    """
    poses = [start_pose.copy()]

    for meas in measurements:
        prev = poses[-1]

        # Rotate local measurement into world frame using current heading
        cos_t = np.cos(prev[2])
        sin_t = np.sin(prev[2])
        world_dx = cos_t * meas[0] - sin_t * meas[1]
        world_dy = sin_t * meas[0] + cos_t * meas[1]

        # New pose = previous + world-frame motion
        new_pose = np.array([
            prev[0] + world_dx,
            prev[1] + world_dy,
            prev[2] + meas[2]
        ])
        poses.append(new_pose)

    return np.array(poses)


# Chain the noisy VO measurements starting from the true origin
drifted_poses = chain_poses(ground_truth[0], vo_measurements)

# Show the drift — how far is the last pose from where it should be?
drift = drifted_poses[-1] - ground_truth[-1]
print(f"Final pose (ground truth): x={ground_truth[-1][0]:.3f}, y={ground_truth[-1][1]:.3f}")
print(f"Final pose (drifted):      x={drifted_poses[-1][0]:.3f}, y={drifted_poses[-1][1]:.3f}")
print(f"Drift: dx={drift[0]:.3f}m, dy={drift[1]:.3f}m, "
      f"dθ={np.degrees(drift[2]):.1f}°")
print(f"Total position drift: {np.sqrt(drift[0]**2 + drift[1]**2):.3f}m")

