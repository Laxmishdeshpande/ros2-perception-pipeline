# ─── 3. Ground Plane Extraction ───────────────────────────
# Find which points are "floor" — critical for navigation

def extract_ground_plane(points, labels, y_threshold=0.5):
    """
    Simple ground extraction: points with Y > threshold
    (remember Y points DOWN in camera coordinates, so large Y = low)

    More sophisticated: RANSAC plane fitting (like findEssentialMat
    but for planes instead of epipolar geometry)

    For navigation: ground points = driveable area
    Non-ground points = obstacles
    """
    # In our coordinate system, ground is at large Y values
    # (camera Y-axis points downward)
    y_values = points[:, 1]
    y_median = np.median(y_values)

    ground_mask = y_values > (y_median + y_threshold)
    obstacle_mask = ~ground_mask

    return ground_mask, obstacle_mask

