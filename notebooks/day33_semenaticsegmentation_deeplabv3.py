# ─── 1. Voxel Downsampling (you did this Day 21) ──────────
# Reduce density while preserving shape
# 874K points is too many for real-time processing
# Downsample to ~1 point per 10cm cube

voxel_size = 0.3  # 30cm voxels
# WHY 0.3m: coarse enough to dramatically reduce points
# Fine enough to preserve human body shape

def voxel_downsample(points, colors, labels, voxel_size):
    """
    Keep one point per voxel (3D cube).
    Same concept as Day 21 Open3D voxel_down_sample,
    but implemented from scratch so you understand it.

    Analogy: imagine dividing 3D space into a grid of cubes.
    If 50 points fall in the same cube, keep only one
    (the average of all 50). Dramatically reduces point count
    while preserving overall shape.
    """
    # Quantize points to voxel grid
    # WHY floor divide: snaps each point to its voxel corner
    # Points at (0.12, 0.34, 0.56) and (0.18, 0.29, 0.51)
    # both snap to (0, 0, 0) with voxel_size=0.3 → same voxel
    voxel_indices = np.floor(points / voxel_size).astype(int)

    # Create unique key for each voxel
    # WHY string key: easy way to hash 3D coordinates
    voxel_dict = {}
    for i in range(len(points)):
        key = tuple(voxel_indices[i])
        if key not in voxel_dict:
            voxel_dict[key] = {'pts': [], 'cols': [], 'lbls': []}
        voxel_dict[key]['pts'].append(points[i])
        voxel_dict[key]['cols'].append(colors[i])
        voxel_dict[key]['lbls'].append(labels[i])

    # Average points within each voxel, majority vote for label
    down_pts, down_cols, down_lbls = [], [], []
    for key, data in voxel_dict.items():
        down_pts.append(np.mean(data['pts'], axis=0))
        down_cols.append(np.mean(data['cols'], axis=0))
        # WHY mean for position/color: smooth average
        # WHY majority vote for label: a voxel on the boundary
        # might have 8 person pixels and 2 bus pixels → label = person
        unique, counts = np.unique(data['lbls'], return_counts=True)
        down_lbls.append(unique[np.argmax(counts)])

    return np.array(down_pts), np.array(down_cols), np.array(down_lbls)

