# ─── 2. Statistical Outlier Removal ───────────────────────
# Remove noisy points that are far from their neighbors

def remove_outliers(points, colors, labels, k=20, std_ratio=2.0):
    """
    For each point, compute mean distance to its K nearest neighbors.
    If a point's mean neighbor distance is more than std_ratio
    standard deviations above the global mean → it's an outlier.

    Analogy: in a crowd, most people stand near others.
    Someone standing 50 meters from everyone else is suspicious
    (probably a noisy measurement). Remove them.

    k=20: check 20 nearest neighbors
    std_ratio=2.0: remove if > 2 standard deviations from mean
    """
    from scipy.spatial import KDTree

    # Build KD-tree for fast nearest neighbor search
    # WHY KDTree: finding K nearest neighbors in 874K points
    # Brute force is O(N^2). KDTree is O(N log N).
    tree = KDTree(points)

    # For each point, find distance to K nearest neighbors
    distances, _ = tree.query(points, k=k+1)
    # WHY k+1: the closest "neighbor" is the point itself (distance=0)
    # Skip the first column (self-distance)
    mean_distances = distances[:, 1:].mean(axis=1)
    # WHY mean of K distances: summarizes how "lonely" each point is

    # Global statistics
    global_mean = mean_distances.mean()
    global_std = mean_distances.std()

    # Keep points within threshold
    threshold = global_mean + std_ratio * global_std
    inlier_mask = mean_distances < threshold
    # WHY this threshold: points with neighbors much farther than average
    # are probably noise. 2 standard deviations catches ~5% outliers.

    return points[inlier_mask], colors[inlier_mask], labels[inlier_mask], inlier_mask

