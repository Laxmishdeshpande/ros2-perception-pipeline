# Step 2: Build Vocabulary Tree using Hierarchical K-Means
#
# Think of it like sorting a library:
# Level 1: Split ALL books into 5 broad categories
# Level 2: Split each category into 5 sub-categories → 25 groups
# Level 3: Split each sub-category into 5 → 125 groups (visual words)
#
# Each "group center" is a visual word — a representative descriptor

from scipy.cluster.vq import kmeans2

def build_vocab_tree(descriptors, branch_factor=5, depth=3):
    """
    Build a hierarchical vocabulary tree.

    branch_factor: how many children each node has (K in k-means)
    depth: how many levels deep the tree goes (D)

    Total visual words = branch_factor^depth = 5^3 = 125
    Comparisons to search = branch_factor × depth = 5 × 3 = 15
    """
    # Convert binary descriptors to float for k-means
    # k-means needs continuous values, not binary
    desc_float = descriptors.astype(np.float32)

    # The tree is stored as a list of levels
    # Each level is a list of (center, child_indices) pairs
    tree = []

    # Level 0: all descriptors start in one big group
    current_groups = [desc_float]

    for level in range(depth):
        next_groups = []
        level_centers = []

        for group in current_groups:
            if len(group) < branch_factor:
                # Group too small to split further
                # Each descriptor becomes its own "center"
                for desc in group:
                    level_centers.append(desc)
                    next_groups.append(np.array([desc]))
                continue

            # K-means: split this group into branch_factor clusters
            centers, labels = kmeans2(group, branch_factor, minit='points', iter=20)

            for k in range(branch_factor):
                mask = labels == k
                if np.sum(mask) > 0:
                    level_centers.append(centers[k])
                    next_groups.append(group[mask])

        tree.append(np.array(level_centers))
        current_groups = next_groups
        print(f'Level {level + 1}: {len(level_centers)} nodes')

    # The leaf nodes are our visual words
    visual_words = tree[-1]
    print(f'\nVocabulary: {len(visual_words)} visual words')
    print(f'Search cost: {branch_factor} × {depth} = {branch_factor * depth} comparisons')
    print(f'Brute force would be: {len(visual_words)} comparisons')

    return tree, visual_words


tree, visual_words = build_vocab_tree(all_desc_combined, branch_factor=5, depth=3)

# Step 3: Convert each frame to a "Bag of Words" histogram
#
# For each frame:
#   1. Take its 300 ORB descriptors
#   2. For each descriptor, find the closest visual word
#   3. Count how often each word appears → histogram
#
# The histogram is the frame's "fingerprint" —
# two frames of the same room will have similar histograms

def descriptor_to_word(desc, visual_words):
    """
    Find the closest visual word for one descriptor.
    Uses Euclidean distance on float descriptors.
    In real ORB-SLAM3, this walks the tree (15 comparisons).
    Here we brute-force for simplicity.
    """
    desc_float = desc.astype(np.float32)
    distances = np.sum((visual_words - desc_float) ** 2, axis=1)
    return np.argmin(distances)


def frame_to_histogram(descriptors, visual_words):
    """
    Convert a frame's descriptors into a Bag of Words histogram.

    Think of it like describing a meal:
    "This plate has 3 parts rice, 2 parts chicken, 1 part salad"
    Instead of listing every grain of rice individually.

    The histogram summarizes WHAT visual patterns appear
    and HOW OFTEN — without caring about WHERE in the image.
    """
    num_words = len(visual_words)
    histogram = np.zeros(num_words)

    for desc in descriptors:
        word_id = descriptor_to_word(desc, visual_words)
        histogram[word_id] += 1

    # Normalize — so frames with different numbers of features
    # can be compared fairly (frame with 500 features shouldn't
    # automatically score higher than frame with 200)
    total = np.sum(histogram)
    if total > 0:
        histogram = histogram / total

    return histogram


