# Step 4: Loop Closure Detection using Histogram Similarity
#
# Compare every pair of frames using cosine similarity.
# Cosine similarity measures how "aligned" two histograms are:
#   1.0 = identical directions (same room)
#   0.0 = completely different
#
# It's like comparing two recipes:
# Both have lots of rice and chicken → similar (same dish)
# One has rice, other has pasta → different (different dish)

def cosine_similarity(hist1, hist2):
    """
    Dot product of two normalized vectors.
    Measures the angle between them — small angle = similar.
    """
    dot = np.dot(hist1, hist2)
    norm1 = np.linalg.norm(hist1)
    norm2 = np.linalg.norm(hist2)
    if norm1 == 0 or norm2 == 0:
        return 0.0
    return dot / (norm1 * norm2)


